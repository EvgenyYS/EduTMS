

public class Seasons
{
    public static void main(String[] args) {
        int num = 6;
        switch (num) {
            case 3:
                System.out.println("Month 3 is Spring");
                break;
            case 6:
                System.out.println("Month 6 is Summer");
                break;
            case 10:
                System.out.println("Month 10 is Autumn");
                break;
        }
    }
}
